    // -----------------------------------------------------------------------------------------------------
    // @ Lifecycle hooks
    // -----------------------------------------------------------------------------------------------------

    /**
     * On init
     */
    ngOnInit(): void
    {
        this.subscription = this.setup.currentChain$.subscribe(async (chain) => {
            await this.updateChainAPI();
          });
        // Get the data
        this._chainAPIService.data$
            .pipe(takeUntil(this._unsubscribeAll))
            .subscribe((data) => {

                // Store the data
                this.data = data;

                // Prepare the chart data
                this._prepareChartData();
            });

        // Attach SVG fill fixer to all ApexCharts
        window['Apex'] = {
            chart: {
                events: {
                    mounted: (chart: any, options?: any): void => {
                        this._fixSvgFill(chart.el);
                    },
                    updated: (chart: any, options?: any): void => {
                        this._fixSvgFill(chart.el);
                    }
                }
            }
        };
    }


    async updateChainAPI() {

        try {
          this.error = null;
          const url = this.setup.Explorer?.ChainAPI?.ApiUrl;

          if (!url) {
            this.chainAPI = {};
            return;
          }

          const request = await this.api.download(url);

          const changePercentage = this.navigator(request, this.setup.Explorer.ChainAPI.PercentagePath);
          let changeType = 'neutral';

          if (changePercentage < 0) {
            changeType = 'negative';
          }

          if (changePercentage > 0) {
            changeType = 'positive';
          }

          this.chainAPI = {
            btc: this.navigator(request, this.setup.Explorer.ChainAPI.PricePathBTC),
            usd: this.navigator(request, this.setup.Explorer.ChainAPI.PricePathUSD),
            changePercentage,
            changeType
          };
        }
        catch (err) {
          this.chainAPI = { btc: null, usd: null, changePercentage: null, changeType: null };
          this.error = err;
        }
      }

      navigator = (obj, path) => {
        for (var i = 0, path = path.split('.'), len = path.length; i < len; i++) {
          if (Array.isArray((obj[path[i]]))) {
            obj = obj[path[i]][path[i + 1]];
            i++;
          } else {
            obj = obj[path[i]];
          }
        };
        return obj;
      };


    /**
     * On destroy
     */
    ngOnDestroy(): void
    {
        // Unsubscribe from all subscriptions
        this._unsubscribeAll.next(null);
        this._unsubscribeAll.complete();
        this.subscription.unsubscribe();
    }

    // -----------------------------------------------------------------------------------------------------
    // @ Public methods
    // -----------------------------------------------------------------------------------------------------

    /**
     * Track by function for ngFor loops
     *
     * @param index
     * @param item
     */
    trackByFn(index: number, item: any): any
    {
        return item.id || index;
    }

    // -----------------------------------------------------------------------------------------------------
    // @ Private methods
    // -----------------------------------------------------------------------------------------------------

    /**
     * Fix the SVG fill references. This fix must be applied to all ApexCharts
     * charts in order to fix 'black color on gradient fills on certain browsers'
     * issue caused by the '<base>' tag.
     *
     * Fix based on https://gist.github.com/Kamshak/c84cdc175209d1a30f711abd6a81d472
     *
     * @param element
     * @private
     */
    private _fixSvgFill(element: Element): void
    {
        // Current URL
        const currentURL = this._router.url;

        // 1. Find all elements with 'fill' attribute within the element
        // 2. Filter out the ones that doesn't have cross reference so we only left with the ones that use the 'url(#id)' syntax
        // 3. Insert the 'currentURL' at the front of the 'fill' attribute value
        Array.from(element.querySelectorAll('*[fill]'))
             .filter(el => el.getAttribute('fill').indexOf('url(') !== -1)
             .forEach((el) => {
                 const attrVal = el.getAttribute('fill');
                 el.setAttribute('fill', `url(${currentURL}${attrVal.slice(attrVal.indexOf('#'))}`);
             });
    }

